function [Ksf,bi11,bi12] = stator_size(b01,h01)
    %常数向量
    B = [0.0142+2*pi*h01/9-2*b01;0.0112-b01/3-h01];
    %系数矩阵
    A = [4*sqrt(3)-2*pi/9 2*tan(10/180*pi)-pi/9;2/sqrt(3)+1 1+2*tan(10/180*pi)/3];
    
    %解出尺寸参数
    X = A\b;
    h11 = X(1,1);
    h21 = X(2,1);
    %计算得到其余尺寸
    r21 = sqrt(3)*h11 +h21*tan(10/180*pi)+0.5*b01;
    b11 = b01 + 2*sqrt(3)*h11;
    %计算面积与槽满率
    As = (2*r21+b11)/2*(h11+h21-0.002)+(pi*r21^2)/2;
    Ai = 0.00025*(2*(h11+h21)+pi*r21+2*r21+b11);
    Aef = As-Ai;
    Ksf = (1*118*(0.68*1e-3)^2)/Aef;
    %计算齿宽
    bi11 = pi*(0.0672+2*h01+2*h11+2*h21)/18 -2*r21;
    bi12 = pi*(0.0672+2*h01+2*h11)/18 -b11;
end